package ro.fasttrackit.course3;

public class HomeworkCourse4 {
    public static void main(String[] args) {

        int num = 6;
        int factorial = 1;
        for (int i = 1; i <= num; i++) {
            factorial = factorial * i;
            System.out.println("Factorial of " + num + " is: " + factorial);
        }
    }
}










